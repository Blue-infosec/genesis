package model.language;

/**
 * This class contains constants that can be used in other classes, especially
 * during the generation of the code. This approach increases the readability of
 * the code and is less prone to errors.
 *
 * @author Max 'Libra' Kersten [@LibraAnalysis]
 */
public class ClassConstants {

    /**
     * A single space
     */
    public static final String SPACE = " ";

    /**
     * A single comma: <code>,</code>
     */
    public static final String COMMA = ",";

    /**
     * A newline character: <code>\n</code>
     */
    public static final String NEW_LINE = "\n";

    /**
     * A set of brackets: <code>()</code>
     */
    public static final String BRACKETS = "()";

    /**
     * The opening bracket: <code>(</code>
     */
    public static final String BRACKET_OPEN = "(";

    /**
     * The closing bracket: <code>)</code>
     */
    public static final String BRACKET_CLOSE = ")";

    /**
     * A set of curly brackets: <code>{}</code>
     */
    public static final String CURLY_BRACKETS = "{}";

    /**
     * The curly opening bracket: <code>{</code>
     */
    public static final String CURLY_OPEN = "{";

    /**
     * The curly closing bracket: <code>}</code>
     */
    public static final String CURLY_CLOSE = "}";

    /**
     * A semicolon: <code>;</code>
     */
    public static final String SEMICOLON = ";";

    /**
     * The equals sign: <code>=</code>
     */
    public static final String EQUALS = "=";

    /**
     * A single space, followed by an equals sign and another space
     */
    public static final String SET_EQUALS = " = ";

    /**
     * A single tab: <code>\t</code>.
     */
    public static final String TAB = "\t";
    
    /**
     * The plus sign: <code>+</code>.
     */
    public static final String PLUS = "+";
}
